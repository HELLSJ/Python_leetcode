## 解法1： 优先队列

# 对于「最大值」，我们可以想到一种非常合适的数据结构，那就是优先队列（堆），其中的大根堆可以帮助我们实时维护一系列元素中的最大值。
#
# 对于本题而言，初始时，我们将数组 nums 的前 k 个元素放入优先队列中。每当我们向右移动窗口时，我们就可以把一个新的元素放入优先队列中，此时堆顶的元素就是堆中所有元素的最大值。
# 然而这个最大值可能并不在滑动窗口中，在这种情况下，这个值在数组 nums 中的位置出现在滑动窗口左边界的左侧。
# 因此，当我们后续继续向右移动窗口时，这个值就永远不可能出现在滑动窗口中了，我们可以将其永久地从优先队列中移除。
#
# 我们不断地移除堆顶的元素，直到其确实出现在滑动窗口中。此时，堆顶元素就是滑动窗口中的最大值。
# 为了方便判断堆顶元素与滑动窗口的位置关系，我们可以在优先队列中存储二元组 (num,index)，表示元素 num 在数组中的下标为 index。

import heapq
def maxSlidingWindow(self, nums, k):
    n = len(nums)

    # 构建一个最大堆，但 Python 只有最小堆，所以存 (-值, 下标)
    # 先把前 k 个元素放进堆中
    q = [(-nums[i], i) for i in range(k)]
    heapq.heapify(q)   # 建堆 O(k)

    # 堆顶元素就是当前窗口的最大值（注意要取负号）
    ans = [-q[0][0]]

    # 从第 k 个元素开始往右滑动窗口
    for i in range(k, n):
        # 将新进入窗口的元素压入堆
        heapq.heappush(q, (-nums[i], i))

        # 堆顶的元素可能已经滑出窗口范围，需要不断弹出
        # 只保留下标在 [i-k+1, i] 这个窗口中的元素
        while q[0][1] <= i - k:
            heapq.heappop(q)

        # 此时堆顶一定是窗口内最大的值
        ans.append(-q[0][0])

    return ans

# 解法2：单调队列

# 我们可以顺着方法一的思路继续进行优化。
#
# 由于我们需要求出的是滑动窗口的最大值，如果当前的滑动窗口中有两个下标 i 和 j，其中 i 在 j 的左侧（i<j），并且 i 对应的元素不大于 j 对应的元素（nums[i]≤nums[j]），那么会发生什么呢？
#
# 当滑动窗口向右移动时，只要 i 还在窗口中，那么 j 一定也还在窗口中，这是 i 在 j 的左侧所保证的。
# 因此，由于 nums[j] 的存在，nums[i] 一定不会是滑动窗口中的最大值了，我们可以将 nums[i] 永久地移除。
#
# 因此我们可以使用一个队列存储所有还没有被移除的下标。在队列中，这些下标按照从小到大的顺序被存储，并且它们在数组 nums 中对应的值是严格单调递减的。
# 因为如果队列中有两个相邻的下标，它们对应的值相等或者递增，那么令前者为 i，后者为 j，就对应了上面所说的情况，即 nums[i] 会被移除，这就产生了矛盾。
#
# 当滑动窗口向右移动时，我们需要把一个新的元素放入队列中。为了保持队列的性质，我们会不断地将新的元素与队尾的元素相比较，如果前者大于等于后者，那么队尾的元素就可以被永久地移除，我们将其弹出队列。
# 我们需要不断地进行此项操作，直到队列为空或者新的元素小于队尾的元素。
#
# 由于队列中下标对应的元素是严格单调递减的，因此此时队首下标对应的元素就是滑动窗口中的最大值。
# 但与方法一中相同的是，此时的最大值可能在滑动窗口左边界的左侧，并且随着窗口向右移动，它永远不可能出现在滑动窗口中了。因此我们还需要不断从队首弹出元素，直到队首元素在窗口中为止。
#
# 为了可以同时弹出队首和队尾的元素，我们需要使用双端队列。满足这种单调性的双端队列一般称作「单调队列」。

import collections

def maxSlidingWindow(self, nums, k):
    n = len(nums)

    # 创建一个双端队列，用来保存“可能成为最大值的下标”
    # 队列中的下标对应的值会保持从大到小排列（单调递减队列）
    q = collections.deque()

    # 先处理前 k 个元素，建立初始窗口
    for i in range(k):
        # 当前元素 nums[i] 如果比队尾大的话，队尾元素不可能成为窗口最大值，弹出
        while q and nums[i] >= nums[q[-1]]:
            q.pop()
        # 把当前下标加入队尾
        q.append(i)

    # 此时队首下标指向窗口中的最大值
    ans = [nums[q[0]]]

    # 继续处理剩下的元素，窗口不断右移
    for i in range(k, n):
        # 将新元素加入队列，同时维持队列单调递减
        while q and nums[i] >= nums[q[-1]]:
            q.pop()
        q.append(i)

        # 如果队首下标已经滑出窗口范围 (i - k 是左边界 - 1)
        # 就把它从队首弹出
        while q[0] <= i - k:
            q.popleft()

        # 当前窗口最大值就是队首下标对应的那个值
        ans.append(nums[q[0]])

    return ans


